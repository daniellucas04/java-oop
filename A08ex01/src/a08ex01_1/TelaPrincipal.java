    private void labelClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelClicked
        javax.swing.JLabel label = (javax.swing.JLabel) evt.getSource();
        
        if(!label.isEnabled())
            return;

        if (evt.getButton() == 1) {
            if (!(label.getText().isEmpty())) {
                int numero = Integer.parseInt(label.getText());
                numero++;
                numero = (numero > 9) ? 1 : numero;
                label.setText((numero) + "");
            } else {
                label.setText("1");
            }
        } else if (evt.getButton() == 2) {
            label.setText("");
        } else if (evt.getButton() == 3) {
            if (!(label.getText().isEmpty())) {
                int numero = Integer.parseInt(label.getText());
                numero += 3;
                numero = (numero > 9) ? 3 : numero;
                label.setText((numero) + "");
            } else {
                label.setText("3");
            }
        }
        
        this.validacao();
    }//GEN-LAST:event_labelClicked

