    private void initializeButtons() {
        this.botoes = new javax.swing.JButton[]{button0, button1, button2, button3, button4, button5, button6, button7, button8, button9, button10, button11, button12, button13, button14, button15};
        
        for(javax.swing.JButton botao: botoes) {
            botao.setText("");
            botao.setEnabled(true);
        }
        
        lblJogadas.setText("00");
        this.jogadas = 0;
        this.primeiraCarta = -1;

        this.shuffle();
    }
    
    private void verifyClick(int index){
        if(index == this.primeiraCarta)
            return;
        
        lblJogadas.setText(this.jogadas + "");
        
        this.botoes[index].setText(cartas[index] + "");
        
        lblJogadas.setText(++this.jogadas + "");

        if(this.primeiraCarta == -1) {
            this.primeiraCarta = index;
        } else {
            if(cartas[this.primeiraCarta] == cartas[index]) {
                JOptionPane.showMessageDialog(rootPane, "Acertou!");
                
                botoes[index].setEnabled(false);
                botoes[this.primeiraCarta].setEnabled(false);
                this.contador++;
            } else {
                JOptionPane.showMessageDialog(rootPane, "Tente novamente!");
             
                botoes[this.primeiraCarta].setText("");
                botoes[index].setText("");
            }
            
            if(this.contador == 8) {
                JOptionPane.showMessageDialog(rootPane, "Voc√™ ganhou!");
                if(this.jogadas < this.record) {
                    this.record = this.jogadas;
                    lblRecord.setText(this.jogadas + "");
                }                
            }
            
            this.primeiraCarta = -1;
        }
    }
    
    private void shuffle() {
        for(int i=0; i<=15; i++) {
            int x = (int) (Math.random() * 15);
            int temp = cartas[x];
            cartas[x] = cartas[i];
            cartas[i] = temp;
        }
    }
