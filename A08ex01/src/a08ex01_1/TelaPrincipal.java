    private void sortear(String dificuldade) {
        
        for(javax.swing.JLabel label: labels) {
            label.setText("");
        }
        
        if(dificuldade.equals("facil")) {
            // fácil = 20 posições
            for(int i=0; i<20; i++) {
                int posicao = (int) (Math.random() * 81);
                int numero = (int) (Math.random() * 9);
                labels[posicao].setText(numero + "");
                labels[posicao].setEnabled(false);
            }
        }
        
        if(dificuldade.equals("medio")) {
            // Médio = 10 posições preenchidas (padrão)
            for(int i=0; i<10; i++) {
                int posicao = (int) (Math.random() * 81);
                int numero = (int) (Math.random() * 9);
                labels[posicao].setText(numero + "");
                labels[posicao].setEnabled(false);
            }
        }
        
        if(dificuldade.equals("dificil")) {
            // dificil = 5 posições
            for(int i=0; i<5; i++) {
                int posicao = (int) (Math.random() * 81);
                int numero = (int) (Math.random() * 9);
                labels[posicao].setText(numero + "");
                labels[posicao].setEnabled(false);
            }            
        }
    }

